#include <unistd.h>

int	ft_putnbr(int nb)
{
	int	x;

	if (nb < 10)
	{
		x = nb + '0';
		write(1, &x, 1);
		return (0);
	}
	ft_putnbr(nb / 10);
	x = (nb % 10) + '0';
	write(1, &x, 1);
}
