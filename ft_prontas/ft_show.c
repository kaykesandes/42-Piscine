#include <unistd.h>

int	show(int nb)
{
	int	x;

	if (nb < 10)
	{
		x = nb + '0';
		write(1, &x, 1);
		return (0);
	}
	show(nb / 10);
	x = (nb % 10) + '0';
	write(1, &x, 1);
}
